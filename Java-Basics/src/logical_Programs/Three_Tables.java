package logical_Programs;

public class Three_Tables 
{
	public static void main(String[] args) 
	{	
		int x = 1;
		while(x<=10)
		{
			System.out.println(3+" X "+x+" = "+(3*x));
			x++;
		}
		
		System.out.println("-------------------------");
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(3+" X "+i+" = "+(3*i));
		}
	}
}

























